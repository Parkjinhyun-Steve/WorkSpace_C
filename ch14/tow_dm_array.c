
#include <stdio.h>

void main()
{
    int s[3][5] = {{20,20,10,40,50},
                   {50,60,40,80,30},
                   {20,80,90,65,72}};

    // 3(행, row) * 5(열, colum )=15개 변수
    // ex)1차 배열 변수 초기화 s[3] = {1,2,3}

    int i,j;

    for ( i = 0; i < 3; i++)
    {
        for ( j = 0; j < 5; j++)
        {
            printf("%[%d][%d]=%d\n", i,j,s[i][j]);
        }
        
    }

}
